package com.Lambda_Expression.Function.Emp;

public class Emp {
	String name;
	int id;
	int age;
	String state;
	
	public Emp(String name, int id, int age, String state) {
		this.name = name;
		this.id = id;
		this.age = age;
		this.state = state;
	}

	@Override
	public String toString() {
		return "Emp [name=" + name + ", id=" + id + ", age=" + age + ", state=" + state + "]";
	}
	
	
	
	
	

}
